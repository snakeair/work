</h4>
<h4>
  2．我们积极建立信息安全管理规范标准，如：信息分类分级制度、安全开发生命周期规范来管理规范个人信息的存储和使用，确保未收集与我们提供的服务无关的个人信息。
</h4>
<h4>
  3．我们会采取一切合理可行的措施，保护您的个人信息。例如，在您的浏览器与我们之间交换数据（如银行卡信息）时受 SSL 加密保护；对我们的网站提供 HTTPS
  安全浏览方式；会使用加密技术、并不断更新密钥来确保数据的保密性，并使用受信赖的保护机制防止数据遭到恶意攻击；以及定期进行信息安全能力评估/信息安全风险评估，定期进行数据库的容灾备份，确保安全保护系统始终处于完备状态。
</h4>
<h4>
  （二）建立严格的管理制度和流程以保障信息的安全
</h4>
<h4>
