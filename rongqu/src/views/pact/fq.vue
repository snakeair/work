</h4>
<h4>
  （四）安全事件处置
</h4>
<h4>
  1．我们将尽力确保您发送给我们的任何信息的安全性，但请您理解，由于技术的限制以及在互联网行业可能存在的各种恶意手段，即便竭尽所能加强安全措施，也不能完全避免与个人信息安全相关的风险（特别是由于我们可控范围外的因素而引发问题时，如不可抗力等）。为防止安全事故的发生，我们制定了妥善的预警机制和应急预案。如果我们知悉其所获取并储存的数据安全受到危害或由于外部行为（包括但不限于外部安全攻击）使用户的非公开信息被披露给不相关第三方，尽管本规则中有其他免责规定，我们仍将采取其认为适当的合理措施（包括但不限于内部调查、上报并通知执法机构、以及配合执法机构工作等）。
